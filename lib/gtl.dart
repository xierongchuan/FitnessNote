Widget mIcon(
    IconData? icon,
    {
      Color color = Colors.white,
      double size = 24.0
    }
    ) {
  return Icon(
    icon ?? Icons.ad_units,
    color: color,
    size: size,
  );
}

Widget mText100(
    String? text,
    {
      double size = 100.0,
      String family = 'Google',
      Color color = Colors.white,
      Color backgroundColor = Colors.transparent,
      FontWeight fontWeight = FontWeight.normal
    }
    ) {
  return Text(
    text ?? '',
    style: TextStyle(
      fontFamily: family,
      fontSize: size,
      color: color,
      backgroundColor: backgroundColor,
      fontWeight: fontWeight
    ),
  );
}

